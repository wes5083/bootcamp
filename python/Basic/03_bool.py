print("----------bool value is False-----------------")
print(bool(False))  # False bool value is False
print(bool(0))      # int 0
print(bool(0.0))    # float 0
print(bool(None))   # None
print(bool(''))     # null character
print(bool(""))     # null str
print(bool([]))     # None array
print(bool(list())) # None list
print(bool(()))     # None tuple
print(bool({}))     # None tuple
print(bool(dict())) # None dict
print(bool(set()))  # None set


